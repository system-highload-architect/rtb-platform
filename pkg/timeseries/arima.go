/*

`arima.go` из изначального плана предназначался для **ARIMA (AutoRegressive Integrated Moving Average)** —
классической статистической модели прогнозирования временных рядов.
Она более мощная и гибкая, чем метод Хольт‑Уинтерса, который мы уже реализовали,
потому что может улавливать сложные несезонные паттерны, стационарные и нестационарные процессы,
включая авторегрессию и скользящее среднее.

**Что ARIMA дала бы системе?**
- Прогноз цены аукциона (ставок) на ближайшие минуты с учётом трендов и шоков.
- Предсказание количества запросов/трафика для планирования ресурсов (автоскейлинг).
- Детекцию аномалий в финансовых потоках (например, резкое падение конверсии).

**Почему мы пока остановились на Holt‑Winters?**
- ARIMA требует подбора параметров (p, d, q) и сезонных (P, D, Q, s), что вычислительно сложнее и нуждается в тщательной офлайн-подготовке (например, через auto.arima на исторических данных).
- Holt‑Winters легче инициализировать и интерпретировать, а для многих бизнес-задач в рекламе (недельная/суточная сезонность) его точности достаточно.
- Мы можем добавить ARIMA позже в аналитический сервис, когда появится потребность в более точном прогнозе спроса или ставок, но не для горячего RTB-пути.
*/
